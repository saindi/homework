let a = Number(prompt('Введіть перше число'));
let b = Number(prompt('Введіть друге число'));

let sumNumbers = a + b;
let differenceNumbers = a - b;
let multiplicationNumbers = a * b;
let divisionNumbers = a / b;

alert(`${a} + ${b} = ${sumNumbers}
${a} - ${b} = ${differenceNumbers}
${a} * ${b} = ${multiplicationNumbers}
${a} / ${b} = ${divisionNumbers}`);
